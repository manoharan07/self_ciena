package com.sify.network.alarms.ciena;

import java.util.ArrayList;

public class DeviceCurrentAlarms {
	
	String ip;
	ArrayList<Notification> alarmList;
	
	public DeviceCurrentAlarms(String ip, ArrayList<Notification> alarmList) {
		this.ip=ip;
		this.alarmList=alarmList;
	}
	
	@Override
	public String toString() {
		return "DeviceCurrentAlarms [ip=" + ip + ", alarmList=" + alarmList + "]";
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public ArrayList<Notification> getAlarmList() {
		return alarmList;
	}

	public void setAlarmList(ArrayList<Notification> alarmList) {
		this.alarmList = alarmList;
	}
	
	

}
